#include <stddef.h>

void	*ft_bzero(void *s, size_t n)
{
	size_t			count;
	unsigned char	*ptr;

	count = 0;
	ptr = (unsigned char *)s;
	while (count < n)
	{
		ptr[count] = 0;
		count++;
	}
	return (s);
}
